package Cartas;

public class Carta_Objeto extends Carta {

	///////////////////////////////////////////////////////////////////////////////////////////////
	////ATRIBUTOS
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	private int tipo;
	private int cantidad;
	
	///////////////////////////////////////////////////////////////////////////////////////////////
	////CONSTRUCTORES
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	public Carta_Objeto(int id, int t, int c) {
		super(id);
		tipo = t;
		cantidad = c;
	}

	///////////////////////////////////////////////////////////////////////////////////////////////
	////METODOS
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	public int getCantidad() {
		return cantidad;
	}
	
	public int getTipo() {
		return tipo;
	}
	
	@Override
	public boolean equals(Object o) {
		return (o instanceof Carta_Objeto) && this.getId() == ((Carta_Objeto)o).getId() && this.hashCode() == ((Carta_Objeto) o).hashCode();
	}
	
	public int hashCode() {
		return Integer.hashCode(this.getId());
	}
	
}
