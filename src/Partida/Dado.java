package Partida;

import java.util.Random;

public class Dado {
	
	///////////////////////////////////////////////////////////////////////////////////////////////
	////ATRIBUTOS
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	private final int caras = 6;
	private int valor;
	private boolean usado;
	private Random r = new Random();
	
	///////////////////////////////////////////////////////////////////////////////////////////////
	////CONSTRUCTORES
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	public Dado() {
		
	}
	
	///////////////////////////////////////////////////////////////////////////////////////////////
	////METODOS
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	public void tirarDado() {
		usado = false;
		this.valor = r.nextInt(caras) + 1;
	}
	
	public int getValor() {
		return valor;
	}
	
	public int usar() {
		usado = true;
		return valor;
	}
	
	public boolean usado() {
		return usado;
	}
	
}
