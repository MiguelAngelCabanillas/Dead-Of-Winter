package Cartas;

public class Carta_Crisis extends Carta{
	
	///////////////////////////////////////////////////////////////////////////////////////////////
	////ATRIBUTOS
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	private Carta_Objeto objeto;
	private String efecto;
	private String opcional;
	
	///////////////////////////////////////////////////////////////////////////////////////////////
	////CONSTRUCTORES
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	public Carta_Crisis(int id, Carta_Objeto objeto, String efecto, String op) {
		super(id);
		this.objeto = objeto;
		this.efecto = efecto;
		this.opcional = op;
	}	
	
	///////////////////////////////////////////////////////////////////////////////////////////////
	////METODOS
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	public Carta_Objeto getObjeto() {
		return this.objeto;
	}
	
	public String getEfecto() {
		return this.efecto;
	}

	public String getOpcional() {
		return opcional;
	}

}
