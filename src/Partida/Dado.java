package Partida;

import java.util.concurrent.ThreadLocalRandom;

public class Dado {
	
	///////////////////////////////////////////////////////////////////////////////////////////////
	////ATRIBUTOS
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	private final int caras = 6;
	private int valor;
	private boolean usado;
	
	///////////////////////////////////////////////////////////////////////////////////////////////
	////CONSTRUCTORES
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	public Dado() {
		
	}
	
	///////////////////////////////////////////////////////////////////////////////////////////////
	////METODOS
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	public void tirarDado() {
		usado = false;
		this.valor = ThreadLocalRandom.current().nextInt(1,this.caras + 1);
	}
	
	public int getValor() {
		return valor;
	}
	
	public int usar() {
		usado = true;
		return valor;
	}
	
	public boolean usado() {
		return usado;
	}
	
}
