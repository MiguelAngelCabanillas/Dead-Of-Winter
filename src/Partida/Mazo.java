package Partida;

import java.util.Stack;

import Cartas.Carta;

public class Mazo {
	
	///////////////////////////////////////////////////////////////////////////////////////////////
	////ATRIBUTOS
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	private Stack<Carta> Mazo = new Stack<>();
	
	///////////////////////////////////////////////////////////////////////////////////////////////
	////CONSTRUCTORES
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	public Mazo(Stack<Carta> mazo) {
		Mazo = mazo;
	}
	
	///////////////////////////////////////////////////////////////////////////////////////////////
	////METODOS
	///////////////////////////////////////////////////////////////////////////////////////////////
	
	public Carta getCarta() {
		return Mazo.pop();
	}
	
	public boolean vacio() {
		return Mazo.isEmpty();
	}
	
}
